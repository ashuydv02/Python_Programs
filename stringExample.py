string = "    Hello, World!    "

print("1. strip():", string.strip())

print("2. lower():", string.lower())

print("3. upper():", string.upper())

print("4. replace('Hello', 'Hi'):", string.replace('Hello', 'Hi'))

print("5. split():", string.split())

print("6. find('World'):", string.find('World'))

print("7. count('l'):", string.count('l'))

print("8. capitalize():", string.capitalize())

print("9. isalpha():", string.isalpha())

print("10. isnumeric():", string.isnumeric())

print("11. isspace():", string.isspace())

print("12. join(['Python', 'Programming']):", '-'.join(['Python', 'Programming']))

print("13. startswith('Hello'):", string.startswith('Hello'))

print("14. endswith('World!'):", string.endswith('World!'))

print("15. format(name='Alice'):", "Hello, {}!".format('Alice'))

print("16. center(30):", string.center(30))

print("17. lstrip():", string.lstrip())

print("18. rstrip():", string.rstrip())

print("19. title():", string.title())

print("20. zfill(20):", string.zfill(20))
