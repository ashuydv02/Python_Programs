# Example 1: Iterate over a list
numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num)

# Example 2: Iterate over a range of numbers
for i in range(1, 6):
    print(i)

# Example 3: Iterate over characters in a string
for char in "Hello":
    print(char)

# Example 4: Nested loops
for i in range(1, 4):
    for j in range(1, 4):
        print(f"{i}, {j}")
